saldo = 200
saque = 200

print (saldo == saque) #saldo é igual a saque?
print (saldo != saque) #saldo é diferente de saque?
print (saldo > saque) #saldo é maior que saque?
print (saldo < saque) #saldo é menor que saque?
print (saldo >= saque) #saldo é maior ou igual a saque?
print (saldo <= saque) #saldo é menor ou igual a saque?