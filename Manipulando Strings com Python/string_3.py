nome = "Rafael Viana Jeronymo"

print(nome [0]) #pega o indice 0 que seria a letra R

print(nome [-1]) #pega a ultima letra

print(nome [:7]) #dois ele ignora o inicio e o segundo argumento é onde ele deve parar no Viana, pq ele sempre volta uma casa

print(nome [7:]) #colocando o inicio 7 ele vai pular e pegar o restante que seria Viana para frente

print(nome [7:13]) #daqui ele vai pegar apanas o viana

print(nome [7:13:2]) #conforme o primeiro e o segundo argumento vai pegar a palavra viana e o terceiro argumento vai pular 2 palavras e pagar a o restante

print(nome [:]) #não passou nenhum argumento ele pega tudo que está na string

print(nome [::-1]) #nao passou nenhum argumento no inicio, nem no stop, e no step passando -1 ele inverte a palavra

